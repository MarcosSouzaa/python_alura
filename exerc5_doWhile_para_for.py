# Modificando do while para o for

# Utilizando while
contador = 1
while(contador <= 10):
    print(contador)
    contador = contador + 1

# Utilizando For
for contador in range(1, 11):
    print(contador)

# Utilizando While iterando de 3 em 3
contador = 1
while(contador <= 10):
    print(contador)
    contador = contador + 3

# Utilizando For iterando de 3 em 3
for contador in range(1, 11, 3):
    print(contador)
